		<p class="name">Introduction to the Practice of Statistics (6th edition) in R</p>

<p>These files are intended to help 
describe how to undertake analyses introduced as examples in the first chapters of the Sixth Edition of <a href="http://bcs.whfreeman.com/ips6e/" target="_blank">Introduction to the Practice of Statistics</a> (2007), 
the excellent text by David Moore, George McCabe and Bruce Craig, using R.

Similar materials are available for the 
<a href="http://www.amherst.edu/~nhorton/sleuth">second</a> and
<a href="http://www.math.smith.edu/~nhorton/sleuth3">third</a>
editions of
the <i>Statistical Sleuth</i>.
</p>

<p>
This work leverages efforts undertaken by <a href="http://www.mosaic-web.org" target="_blank">Project MOSAIC</a>, an NSF-funded initiative to improve the teaching of statistics, calculus, science and computing in the undergraduate curriculum. In particular, we utilize the 
<a href="http://cran.r-project.org/web/packages/mosaic" target="_blank">mosaic</a> package, which was written to simplify the use of R for introductory statistics courses. 
A short summary of the R needed to teach introductory statistics can be found in the mosaic package vignette. located 
<a href="http://cran.r-project.org/web/packages/mosaic">here</a>.</p>

<p>Other R packages used include MASS, agricolae, car, gmodels, leaps and xtable.</p>
<p>
We have include both formatted pdf files as well as the original knitr files which were used to generate the output. <a href="http://yihui.name/knitr/">Knitr</a> is an elegant, flexible and fast means to undertake reproducible analysis
and dynamic report generation within R and <a href="http://www.rstudio.org" target="_blank">RStudio</a>.  Comments, suggestions and corrections welcomed.

<p>
Nicholas Horton and Ben Baumer (last updated January 12, 2014)</p>
